#!/usr/bin/env bash
# Regenerate refcodec.js — gputex's CPU reference block codecs (MIT), bundled
# for the browser. bench.js decodes BOTH gputex's and spark's compressed output
# with these to compute PSNR, so the quality comparison uses one validated
# decoder per format rather than hand-rolled ones.
#
# Requires `bun` and `git`. Only needed when gputex updates its reference
# codecs — the committed refcodec.js is what the benchmark loads at run time
# (which needs only node + Chrome, no bun).
#
#   bash build-refcodec.sh                 # clones gputex fresh
#   GPUTEX_SRC=/path/to/gputex bash build-refcodec.sh   # use a local checkout
set -euo pipefail
cd "$(dirname "$0")"

if [ -n "${GPUTEX_SRC:-}" ]; then
  SRC="$GPUTEX_SRC"
  CLEANUP=""
else
  SRC="$(mktemp -d)/gputex"
  git clone --depth 1 https://github.com/verekia/gputex.git "$SRC"
  CLEANUP="$(dirname "$SRC")"
fi

bun build "$SRC/library/src/testing.ts" --format=esm --target=browser --outfile=refcodec.tmp.js

# Sanitize bun's module-separator comments (absolute/temp build paths) to a
# stable form so the committed file is identical no matter where it was built.
sed -E -i '' 's|^// .*/(library/src/[^/]+\.ts)$|// gputex/\1|' refcodec.tmp.js 2>/dev/null \
  || sed -E -i 's|^// .*/(library/src/[^/]+\.ts)$|// gputex/\1|' refcodec.tmp.js

{
  echo "// refcodec.js — gputex CPU reference block codecs (BC1 / BC4 / BC5 / BC7 modes 1,4,6 / ASTC 4x4 LDR)."
  echo "// Bundled verbatim from https://github.com/verekia/gputex (library/src/testing.ts, MIT)."
  echo "// Generated by build-refcodec.sh — do not edit by hand."
  echo "//"
  echo "// bench.js decodes both gputex's and spark's output with these to compute PSNR."
  echo "// NOTE: decodeBC7Block covers modes 1/4/6 (throws on 0/2/3/5/7) and decodeASTC4x4Block is"
  echo "// block-mode-0x042/CEM-12 only (gputex's own subset; throws on spark's ASTC config) —"
  echo "// bench.js scores with bc7full.js (modes 4/5/6) and handles the ASTC fallback."
  cat refcodec.tmp.js
} > refcodec.js
rm -f refcodec.tmp.js
[ -n "$CLEANUP" ] && rm -rf "$CLEANUP"
echo "wrote refcodec.js ($(wc -l < refcodec.js) lines)"
